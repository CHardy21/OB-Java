package Poo.clases;

public class Motor {
	
	String modeloMotor;
	int horsePower;
	int numCilindros;
	double parNm;
	
	public Motor() {}

	public Motor(String modeloMotor, int horsePower, int numCilindros, double parNm) {
		super();
		this.modeloMotor = modeloMotor;
		this.horsePower = horsePower;
		this.numCilindros = numCilindros;
		this.parNm = parNm;
	}
	
	
	

}
